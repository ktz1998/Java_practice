public class Ready01 {
  public static void main(String[] args){
    /*
    public...クラス内関数が外部からアクセス可能 →main関数はどこからでも利用できる
    static...データ型であるクラス内の関数を利用可能に 
              →実体として形が作られていないクラスの関数を使うため
    void...戻り値 →main関数は戻り値を必要としない
    main...関数名
    (String[] args)...引数・String型の文字列を扱う配列
    */
    System.out.println("HelloWorld!");
  }
}